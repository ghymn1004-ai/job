export function getPosts(): Post[] {
  const localData = localStorage.getItem('iium_posts');
  if (!localData) {
    localStorage.setItem('iium_posts', JSON.stringify(SEED_POSTS));
    return SEED_POSTS;
  }
  try {
    const parsed: Post[] = JSON.parse(localData);
    if (parsed.length < SEED_POSTS.length) {
      const merged = [...parsed];
      SEED_POSTS.forEach(sp => {
        if (!merged.some(p => p.id === sp.id)) {
          merged.push(sp);
        }
      });
      localStorage.setItem('iium_posts', JSON.stringify(merged));
      return merged;
    }
    return parsed;
  } catch (e) {
    return SEED_POSTS;
  }
}

